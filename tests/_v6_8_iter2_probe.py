    wall=time.time()-t0
    ans=(r.get("answer") or "")[:200].encode("ascii","replace").decode("ascii")
    print(f'[{wall:5.2f}s {r.get("tier","?"):<27}] {msg!r}',flush=True)
    print(f'  -> {ans}\n',flush=True)
